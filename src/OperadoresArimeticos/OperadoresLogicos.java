package OperadoresArimeticos;

public class OperadoresLogicos {
    public static void main(String[] args){
    //Operadores logicos and(&&), or(), not

        boolean resultado;

        resultado = 5>=5 && true;
        System.out.println(resultado);

        resultado = 5>=5 && true && 10 > 9;
        System.out.println(resultado);

        resultado = 5>=5 && true && 10 > 9;
        System.out.println(resultado);
    }
}
